/****************************************************************************************************//**
 * @fun		drv_GPS_EN_as_output
 * @brief	Provided interface to initialize driver 
 * @return	n/a
 **********************************************************************************/
void drv_GPS_EN_as_output(void){
	drv_GPIO_config_pin(GPS_EN_PIN,GPS_EN_PORT,GPIO_Mode_Out_PP,GPIO_Speed_2MHz);
}

/****************************************************************************************************//**
 * @fun		drv_GPS_EN_On
 * @brief	Provided interface to turn driver on
 * @return	n/a
 **********************************************************************************/
void drv_GPS_EN_On(void){
	#if HARDWARE_RELEASE == HW_REV2
	GPIO_SetBits(GPS_EN_PORT,GPS_EN_PIN);
	#else
	GPIO_ResetBits(GPS_EN_PORT,GPS_EN_PIN);
	#endif
}

/****************************************************************************************************//**
 * @fun		drv_GPS_EN_Off
 * @brief	Provided interface to turn driver off
 * @return	n/a
 **********************************************************************************/
void drv_GPS_EN_Off(void){
	#if HARDWARE_RELEASE == HW_REV2
	GPIO_ResetBits(GPS_EN_PORT,GPS_EN_PIN);
	#else
	GPIO_SetBits(GPS_EN_PORT,GPS_EN_PIN);
	#endif
}

